# 内包表記でforのネスト

# 内包表記のforネストは左が外側、右が内側になる（右から回る）
# 下記の場合、jから回る
li = [(i, j) for i in [1, 2, 3] for j in ["a", "b", "c"]]
print(li)
# 出力：[(1, 'a'), (1, 'b'), (1, 'c'), (2, 'a'), (2, 'b'), (2, 'c'), (3, 'a'), (3, 'b'), (3, 'c')]

# iとjのリストを入れ替える：jから回るから数字から回る
li = [(i, j) for i in ["a", "b", "c"] for j in [1, 2, 3]]
print(li)
# 出力：[('a', 1), ('a', 2), ('a', 3), ('b', 1), ('b', 2), ('b', 3), ('c', 1), ('c', 2), ('c', 3)]
