# 問題集3章 問9.：リスト内包の入れ子＋条件式

### リスト内包の入れ子
li = [(i, j) for i in range(3) for j in range(3)]

print(li)
# 出力：[(0, 0), (0, 1), (0, 2), (1, 0), (1, 1), (1, 2), (2, 0), (2, 1), (2, 2)]

print("li[0]:", type(li[0]), li[0])
# 出力：li[0]: <class 'tuple'> (0, 0)

### リスト内包の入れ子＋条件式
# i == j の時はリストに加えない
li = [(i, j) for i in range(3) for j in range(3) if i != j]

print(li)
# 出力：[(0, 1), (0, 2), (1, 0), (1, 2), (2, 0), (2, 1)]
