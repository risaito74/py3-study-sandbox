#辞書のテスト
dict = {
    '青森': 100,
    '岩手': 200,
    '秋田': 300
}

print(dict)     # 辞書の内容を出力

for key, val in dict.items():  # 辞書のキーを順に取り出す
    print(f"{key}: {val}")  # キーと対応する値を出力

# 辞書のキーを指定して値を取り出す
print(dict['青森'])  # 青森の値を出力

# 辞書に新しいキーと値を追加
dict['宮城'] = 400
print(dict)  # 更新後の辞書を出力

# 辞書のキーを指定して値を更新
dict['岩手'] = 250
print(dict)  # 更新後の辞書を出力

# 辞書からキーを指定して値を削除
del dict['秋田']
print(dict)  # 削除後の辞書を出力

# 辞書のキーを指定して値を取得（存在しない場合はデフォルト値を返す）
print(dict.get('福島', '存在しません'))  # 福島は存在しないのでデフォルト値を出力

# 辞書のキーを指定して値を取得（存在しない場合はNoneを返す）
print(dict.get('青森'))  # 青森は存在するので値を出力
